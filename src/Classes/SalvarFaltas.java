package Classes;
/** Classe que cria um Objeto do tipo SalvarFaltas
 * @author Eduardo Henrique
 */
public class SalvarFaltas {
    int id;
    String nome;
    Object presente;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Object getPresente() {
        return presente;
    }

    public void setPresente(Object presente) {
        this.presente = presente;
    }
}
